import { useCallback, useMemo, useState } from "react";
import {
  ReactFlow,
  ReactFlowProvider,
  Background,
  BackgroundVariant,
  Controls,
  MiniMap,
  Panel,
  useNodesState,
  useEdgesState,
  addEdge,
  Connection,
  Edge,
  Node,
  MarkerType,
  useReactFlow,
} from "@xyflow/react";
import "@xyflow/react/dist/style.css";
import ChatCardNode, { CardMessage, ChatCardData } from "@/components/chat/ChatCardNode";
import { Plus, Sparkles, Trash2, LayoutGrid } from "lucide-react";

const nodeTypes = { chatCard: ChatCardNode };

// --- Demo AI logic ---
const RESPONSES: { match: RegExp; reply: string }[] = [
  {
    match: /jwt/i,
    reply: `A **JWT** is \`header.payload.signature\`. The payload is base64-encoded — not encrypted.
\`\`\`js
import jwt from "jsonwebtoken";
const token = jwt.sign({ sub: "user_1" }, process.env.SECRET, { expiresIn: "1h" });
\`\`\`
Always verify the signature server-side.`,
  },
  {
    match: /random/i,
    reply: `**Randomness** refers to the lack of pattern or predictability in events.

In computing, random numbers are usually generated by *pseudorandom number generators* (PRNGs) — deterministic but appearing random.

\`\`\`js
// Random integer between 1 and 100
const n = Math.floor(Math.random() * 100) + 1;
console.log(n);
\`\`\`

True randomness can come from physical phenomena like atmospheric noise.`,
  },
  {
    match: /sql|duplicate/i,
    reply: `Find duplicates with \`GROUP BY\`:
\`\`\`sql
SELECT email, COUNT(*) AS n
FROM users
GROUP BY email
HAVING COUNT(*) > 1;
\`\`\``,
  },
  {
    match: /cors/i,
    reply: `**CORS** errors happen when your server doesn't whitelist the request origin.
\`\`\`js
app.use(cors({ origin: ["http://localhost:3000"], credentials: true }));
\`\`\``,
  },
  {
    match: /regex|email/i,
    reply: `Practical email regex:
\`\`\`js
const re = /^[^\\s@]+@[^\\s@]+\\.[^\\s@]+$/;
\`\`\``,
  },
];

function generateReply(prompt: string): string {
  for (const r of RESPONSES) if (r.match.test(prompt)) return r.reply;
  return `Here's a thought on **"${prompt.slice(0, 60)}${prompt.length > 60 ? "…" : ""}"**:

This canvas is a workspace for parallel conversations. Drag cards around, resize them, connect them, or branch a reply into a new card.

\`\`\`ts
// Try asking about: jwt, sql, cors, random, regex
function explore(idea: string) {
  return idea.split("").reverse().join("");
}
\`\`\``;
}

